func TestGetDeleteThisLpa(t *testing.T) {
	w := httptest.NewRecorder()
	r, _ := http.NewRequest(http.MethodGet, "/", nil)

	template := newMockTemplate(t)
	template.
		On("Execute", w, &deleteThisLpaData{
			App: testAppData,
			Lpa: &page.Lpa{},
		}).
		Return(nil)

	err := DeleteThisLpa(template.Execute, nil)(testAppData, w, r, &page.Lpa{})
	resp := w.Result()

	assert.Nil(t, err)
	assert.Equal(t, http.StatusOK, resp.StatusCode)
}

